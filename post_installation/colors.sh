# After '[' all numbers modify the original string
# 1 == Bold, 2 == light color, 3 == Italics, 4 == underline, 31 == red, etc
# So all numbers separated by ';' modify the original string

# Colors
export NC='\033[0m' # No Color
export RED='\033[0;31m'
export GREEN='\033[0;32m'
export YELLOW='\033[0;33m'
export BLUE='\033[0;34m'
export PURPLE='\033[0;35m'
export CYAN='\033[0;36m'

export BOLD='\033[1m'
export B_RED='\033[1;31m'
export B_GREEN='\033[1;32m'
export B_YELLOW='\033[1;33m'
export B_BLUE='\033[1;34m'
export B_PURPLE='\033[1;35m'
export B_CYAN='\033[1;36m'

export ITALICS='\033[3m'
export I_RED='\033[3;31m'
export I_GREEN='\033[3;32m'
export I_YELLOW='\033[3;33m'
export I_BLUE='\033[3;34m'
export I_PURPLE='\033[3;35m'
export I_CYAN='\033[3;36m'

export UNDERLINE='\033[4m'
export U_RED='\033[4;31m'
export U_GREEN='\033[4;32m'
export U_YELLOW='\033[4;33m'
export U_BLUE='\033[4;34m'
export U_PURPLE='\033[4;35m'
export U_CYAN='\033[4;36m'
